def preprocess(text: str) -> str:
    """
    Предобработка текста для ML:
    - перевод в нижний регистр
    - удаление спецсимволов и цифр
    - нормализация пробелов

    Args:
        text: Исходный текст
    Returns:
        Очищенный текст
    """
    text = text.lower()
    text = re.sub(r"[^\w\s]", " ", text)
    text = re.sub(r"\d+", " ", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text


def build_and_train_model() -> Pipeline:
    """
    Создаёт и обучает Pipeline: TF-IDF → MultinomialNB.
    Сохраняет модель в models/classifier.pkl.

    Returns:
        Обученный Pipeline sklearn
    """
    texts = [preprocess(t) for t, _ in TRAINING_DATA]
    labels = [label for _, label in TRAINING_DATA]

    pipeline = Pipeline([
        ("tfidf", TfidfVectorizer(
            analyzer="word",
            ngram_range=(1, 2),   # Унаграммы + биграммы
            max_features=5000,
            min_df=1,
            sublinear_tf=True,    # Логарифмирование TF
        )),
        ("clf", MultinomialNB(alpha=0.1)),
    ])

    # Оценка качества на train/test split
    if len(texts) > 10:
        X_train, X_test, y_train, y_test = train_test_split(
            texts, labels, test_size=0.2, random_state=42
        )
        pipeline.fit(X_train, y_train)
        y_pred = pipeline.predict(X_test)
        print("\n[ML] Отчёт классификации:")
        print(classification_report(y_test, y_pred, zero_division=0))
    else:
        pipeline.fit(texts, labels)

    # Переобучаем на всех данных для финальной модели
    pipeline.fit(texts, labels)

    # Сохранение модели
    os.makedirs(os.path.dirname(MODEL_PATH), exist_ok=True)
    with open(MODEL_PATH, "wb") as f:
        pickle.dump(pipeline, f)
    logger.info(f"[ML] Модель сохранена: {MODEL_PATH}")

    return pipeline


# Глобальный экземпляр модели (ленивая инициализация)
_model: Optional[Pipeline] = None


def _get_model() -> Pipeline:
    """Возвращает загруженную или только что обученную модель."""
    global _model
    if _model is not None:
        return _model

    if os.path.exists(MODEL_PATH):
        with open(MODEL_PATH, "rb") as f:
            _model = pickle.load(f)
        logger.info("[ML] Модель загружена из файла.")
    else:
        logger.info("[ML] Обучение новой модели...")
        _model = build_and_train_model()

    return _model


def classify_article(text: str) -> str:
    """
    Классифицирует текст статьи по категориям через ML-модель.

    Args:
        text: Заголовок + описание статьи
    Returns:
        Строка-категория (технологии / политика / спорт / ...)
    """
    if not text or not text.strip():
        return "общее"

    try:
        model = _get_model()
        processed = preprocess(text)
        category = model.predict([processed])[0]
        return category
    except Exception as e:
        logger.error(f"[ML] Ошибка классификации: {e}")
        return "общее"


def classify_with_confidence(text: str) -> tuple[str, float]:
    """
    Классифицирует текст и возвращает уверенность модели.

    Returns:
        Кортеж (категория, уверенность от 0 до 1)
    """
    try:
        model = _get_model()
        processed = preprocess(text)
        proba = model.predict_proba([processed])[0]
        best_idx = np.argmax(proba)
        category = model.classes_[best_idx]
        confidence = float(proba[best_idx])
        return category, confidence
    except Exception as e:
        logger.error(f"[ML] Ошибка: {e}")
        return "общее", 0.0


def get_sentiment(text: str) -> str:
    """
    Определяет эмоциональную окраску текста (sentiment analysis).
    Использует словарный подход — подсчёт позитивных и негативных слов.

    Args:
        text: Текст для анализа
    Returns:
        'позитивный' | 'негативный' | 'нейтральный'
    """
    words = set(preprocess(text).split())
    pos_score = len(words & POSITIVE_WORDS)
    neg_score = len(words & NEGATIVE_WORDS)

    if pos_score > neg_score:
        return "позитивный"
    elif neg_score > pos_score:
        return "негативный"
    return "нейтральный"


